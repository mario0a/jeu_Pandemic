package marqueurs;

public class MarqueurRemede {
    public String recto;
    public String verso;

    public MarqueurRemede(String recto) {
        this.recto = recto;
    }

    public String getRecto() {
        return recto;
    }

    public void setRecto(String recto) {
        this.recto = recto;
    }

    public String getVerso() {
        return verso;
    }

    public void setVerso(String verso) {
        this.verso = verso;
    }
}
